const STORAGE_KEY = "findroom.language";
const EVENT = "findroom:language-change";

function readStored(): Language {
  if (typeof window === "undefined") return "km";
  try {
    const raw = window.localStorage.getItem(STORAGE_KEY);
    return raw === "en" ? "en" : "km";
  } catch {
    return "km";
  }
}

